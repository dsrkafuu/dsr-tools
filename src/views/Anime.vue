<template>
  <div class="fill-height">
    <div class="header">
      <h3>{{ todate }} {{ today }}</h3>
      <span>本季度共 {{ bangumiCount }} 部番组 | 今日上映 {{ todayCount }} 部</span>
    </div>
    <v-overlay absolute :opacity="1" color="white" :value="!status">
      <v-progress-circular indeterminate color="primary"></v-progress-circular>
    </v-overlay>
    <div class="content" v-if="status">
      <div class="day" v-for="date of animeData" :key="date.weekday.id">
        <div :class="['date', { today: today === date.weekday.cn }]">{{ date.weekday.cn }}</div>
        <a v-for="item of date.items" :key="item.id" :href="item.url | filtHttps" target="_blank">
          <div class="anime">
            <img v-if="item.images" class="anime-img" :src="item.images" />
            <div class="anime-name">
              <div class="name-zh">{{ item.name_cn }}</div>
              <div class="name-ja">{{ item.name }}</div>
            </div>
            <div class="anime-rating">{{ item.rating.score | filtRating }}</div>
          </div>
        </a>
      </div>
    </div>
  </div>
</template>

<script>
import CDN_OSS_SUBJECT from '@/utils/bangumi/imageCDN';
import storage from '@/utils/storage';

const HASH_CDN = 'https://cdn.jsdelivr.net/gh/czy0729/Bangumi-OSS@master/hash/subject.json';
const BGM_CDN = 'https://dsr-tools-bgm.dsrkafuu.workers.dev/calendar';

export default {
  name: 'Anime',
  data() {
    return {
      status: false,
      animeData: [],
      cdnHash: {},
    };
  },
  filters: {
    filtHttps(val) {
      return val.replace(/^http/, 'https');
    },
    filtRating(val) {
      if (val === -1) {
        return '-.-';
      } else if (Math.floor(val) === val) {
        return `${val}.0`;
      } else {
        return `${val}`;
      }
    },
  },
  computed: {
    today() {
      const date = new Date();
      const dateArr = ['日', '一', '二', '三', '四', '五', '六'];
      return `星期${dateArr[date.getDay()]}`;
    },
    todate() {
      const date = new Date();
      return `${date.getFullYear()}年${date.getMonth() + 1}月${date.getDate()}日`;
    },
    bangumiCount() {
      if (this.status) {
        let count = 0;
        this.animeData.forEach((val) => {
          count += val.items.length;
        });
        return count;
      } else {
        return '-';
      }
    },
    todayCount() {
      if (this.status) {
        const date = new Date();
        if (date.getDay() === 0) {
          return this.animeData[6].items.length;
        } else {
          return this.animeData[date.getDay() - 1].items.length;
        }
      }
      return '-';
    },
  },
  methods: {
    /**
     * 获取 CDN hash
     */
    async fetchHash() {
      const hash = await this.$axios.get(HASH_CDN);
      this.cdnHash = hash.data;
    },
    async fetchData() {
      const res = await this.$axios.get(BGM_CDN);
      // 补全 rating 数据
      res.data.forEach((val) => {
        val.items.forEach((val) => {
          if (!val.rating) {
            val.rating = { score: -1 };
          }
        });
      });
      this.animeData = res.data;
    },
    async applyCDN() {
      await Promise.all([this.fetchHash(), this.fetchData()]);
      // 替换 CDN 图片
      this.animeData.forEach((val) => {
        val.items.forEach((val) => {
          // api 返回图片类型判断
          if (val.images) {
            if (typeof val.images.common === 'string') {
              const image = CDN_OSS_SUBJECT(val.images.common, this.cdnHash);
              val.images = image;
            } else if (typeof val.images === 'string') {
              const image = CDN_OSS_SUBJECT(val.images, this.cdnHash);
              val.images = image;
            } else {
              delete val.images;
            }
          }
        });
      });
    },
    storeCache() {
      storage.setLS('dsr-tools_anime-cache', JSON.stringify(this.animeData));
      const date = new Date();
      const time = date.getTime();
      storage.setLS('dsr-tools_anime-cache-date', JSON.stringify(time));
      this.$message({ type: 'info', text: '数据已缓存' });
    },
  },
  async mounted() {
    const date = new Date();
    const time = date.getTime();
    try {
      const cacheTime = JSON.parse(storage.getLS('dsr-tools_anime-cache-date'));
      if (time - cacheTime < 86400000) {
        const cache = JSON.parse(storage.getLS('dsr-tools_anime-cache'));
        if (cache.length > 1) {
          this.animeData = cache;
          this.status = true;
          this.$message({ type: 'info', text: '已读取数据缓存' });
          return;
        }
      }
      await this.applyCDN();
      this.status = true;
      this.storeCache();
    } catch (e) {
      this.$message({ type: 'error', text: '缓存操作失败' });
      await this.applyCDN();
      this.status = true;
      this.storeCache();
    }
  },
};
</script>

